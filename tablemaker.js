function StrToDot(str)
{
	var dot = 0;
	for (var i = 0; i < str.length; i++) {
		dot += CharToDot(str.charAt(i));
	}
	return dot;
}
function StrToDotEx(str)
{
	var dot = 0;
	while (/(&[^;]+;|.)/g.exec(str)) {
		var s = RegExp.$1;
		if (s.length > 1) {
			dot += UnicodeCharToDot(s);
		} else {
			dot += CharToDot(s);
		}
	}
	return dot;
}

